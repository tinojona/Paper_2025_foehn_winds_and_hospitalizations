###############################################################################
### FUNCTION TO DETERMINE THE QAIC OF A MODEL
###############################################################################

# q-AIC computation
QAIC <- function(model) {
  phi <- summary(model)$dispersion
  loglik <- sum(dpois(model$y, model$fitted.values, log=TRUE))
  return(-2*loglik + 2*summary(model)$df[3]*phi)
}
